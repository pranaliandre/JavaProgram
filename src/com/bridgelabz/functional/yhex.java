package com.bridgelabz.functional;


public class yhex {
public static void main(String[] args)
{
			for (int i = 3-1; i >= 1; i--)
	        {
	            for (int j = 1; j < i; j++)
	            {
	                System.out.print(" ");
	            }

	            for (int k = i; k <= 3; k++)
	            {
	                System.out.print("* ");
	            }
	            System.out.println();
	         
	        }
	        for (int i = 3-1; i >= 1; i--)
	        {
	            for (int j = 1; j < i; j++)
	            {
	                System.out.print(" *");
	            }
	        }
	        for (int i = 3-1; i >= 1; i--)
	        {
	            for (int j = 1; j < i; j++)
	            {
	                System.out.print(" *");
	            }
	        }
}
}

